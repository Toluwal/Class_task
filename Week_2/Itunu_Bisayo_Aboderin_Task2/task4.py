# # 4. Escape Sequence 
# # - Ask for the name of a Nigerian dish and the state it is popular in. Print the result in two lines using `\n` and add a tab space before the state name using `\t`.

dish = input("What is the name of a popular Nigerian_dish?: ")
origin = input("Which state is  the  Nigerian_dish popular ?: ")
print(f" The Nigerian dish is {dish}.\n It is popular in\t{origin} state.")

